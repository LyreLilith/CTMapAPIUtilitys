namespace MapAPIUTilitys;

public struct Color
{
   
   public float red;
   public float green;
   public float blue;
   public float alpha;

    // Constructor to initialize the color components
    public Color(float red, float green, float blue, float alpha)
    {
        this.red = red;
        this.green = green;
        this.blue = blue;
        this.alpha = alpha;
    }

   
}
